      <h2>一、套利的本质</h2>
      <p>套利的本质是低买高卖，就是从价格低的地方买到，再拿到价格高的地方卖出。如果说有一种套利不是这种机制，那么它实际上不属于套利了。我简要举几个常见的套利例子：</p>
      <ul>
        <li>现货搬砖。这种情况经常发生在新币上线时，一些平台可能会出现剧烈的价格波动，导致不同平台之间出现价差，从而产生套利。</li>
        <li>DEX 和 CEX 套利。比如之前 CRV
          清算时凯文发现链上价格比交易所低很多，于是我就去币安做空CRV，小赚了一笔。为什么会做空呢？因为链上清算只要持续进行，价格一定会更低，导致链上价格比CEX低，而CEX的价格大概率会下跌与链上持平。所以凯文此时做空胜率很大，当然我当时也可以选择去链上买了
          CRV，再拿到 CEX 卖掉，但 Gas费实在太高，而且价格波动太快，容易亏，所以当时选择了做空。</li>
