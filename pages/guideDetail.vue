          目前最常见的最稳定的套利是 Pendle 的PT系列资产，这个凯文之前也有反复讲过。</li>
        <li>场外套利。场外套利这个词，目前资料比较少。凯文在 REZ 套利活动中得到灵感，取名“场外套利”，如果你有更为专业的词，欢迎指正。所谓场外套利，就是利用不同场外平台之间的价差，来进行套利，从而锁定利润。</li>
